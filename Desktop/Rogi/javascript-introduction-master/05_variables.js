// Variables

// New variables in JavaScript are declared using the var keyword:

var a;
var name = "simon";

// If you declare a variable without assigning any value to it, its type is undefined.

// An important difference between JavaScript and other languages like 
// Java is that in JavaScript, blocks do not have scope; only functions 
// have scope. So if a variable is defined using var in a compound statement 
// (for example inside an if control structure), it will be visible to the 
// entire function. However, starting with ECMAScript Edition 6, let and const 
// declarations allow you to create block-scoped variables.
