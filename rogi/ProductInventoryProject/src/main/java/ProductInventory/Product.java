public class Product {

    float price;
    String id;
    float qty;

    public void setPrice(float price) {
        this.price = price;
    }

    public float getPrice() {
        return price;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setQty(float qty) {
        this.qty = qty;
    }

    public float getQty() {
        return qty;
    }


}
